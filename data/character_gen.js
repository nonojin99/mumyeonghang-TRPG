function rollCharacter(rng){
  rng = rng || Math.random;
  var p = function(a){ return a[Math.floor(rng()*a.length)]; };
  var sur = p(PC_SUR);
  var nm = p(PC_NAME) + (rng()<0.5 ? p(PC_NAME) : '');   // 1~2음절
  var bg = p(PC_ORIGINS);
  return {
    성: sur, 이름: sur + nm, 출신: bg.출신, 배경: bg.배경,
    동기: bg.동기, 특성: bg.특성, hook: bg.hook, originId: bg.id
  };
}

